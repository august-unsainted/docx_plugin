const DEFAULT_SYSTEM_FULL = BASE_PROMPT.replace('{задача}', 'Написать полностью (от введения и до заключения) готовую, качественную академическую работу по указанной теме');
const DEFAULT_SYSTEM_PARTIAL = BASE_PROMPT.replace('{задача}', 'Дописать часть академической работы согласно промту пользователя. Раскрой суть фрагмента максимально подробно и содержательно, используя информацию из контекста всей работы. Пиши только запрошенный фрагмент, без лишних пояснений');

export function getSystemPrompt(mode: "full" | "partial", userPrompt: string): string {
	if (userPrompt.trim()) return userPrompt;
	return mode === "full" ? DEFAULT_SYSTEM_FULL : DEFAULT_SYSTEM_PARTIAL;
}

export function buildFullPrompt(topic: string): string {
	return `Тема работы: ${topic}`;
}

export function buildPartialPrompt(
	fullDocument: string,
	task: string
): string {
	return `Контекст всей работы:\n\n${fullDocument}\n\n---\n\nЗадание: ${task}`;
}